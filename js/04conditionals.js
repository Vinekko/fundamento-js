// conditionals en javascript

// evaluacion de edad para adulto mayor

const age = -10;
if (age >= 65) {
    console.log("You are old enough to retire.");
} else if (age >= 18) {
    console.log("You are old enough to vote.");
} else if (age < 0) {
    console.log("Age cannot be negative.");
} else {
    console.log("You are not old enough to vote.");
}

